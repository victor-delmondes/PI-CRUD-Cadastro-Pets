    // Parte do Pet
    private void ListarValoresPet() {
        try {
            Petdao objpetdao = new Petdao();

            DefaultTableModel model = (DefaultTableModel) TableCadastros.getModel();
            model.setNumRows(0);

            ArrayList<Animal> lista = objpetdao.PesquisarPet();

            for (int num = 0; num < lista.size(); num++) {
                if (lista.get(num).getFk_id_user() == id_user) {
                    String petSitStatus;
                    if (lista.get(num).getPet_sit() == 1) {
                        petSitStatus = "Encontrado";
                    } else if (lista.get(num).getPet_sit() == 2) {
                        petSitStatus = "Perdido";
                    } else {
                        petSitStatus = String.valueOf(lista.get(num).getPet_sit()); // Caso haja outros valores possíveis
                    }

                    model.addRow(new Object[]{
                        lista.get(num).getPet_id(),
                        lista.get(num).getPet_nome(),
                        lista.get(num).getPet_especie(),
                        lista.get(num).getPet_raca(),
                        lista.get(num).getPet_cor(),
                        lista.get(num).getPet_detalhes(),
                        petSitStatus
                    });
                }
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "TelaInicioADM - ListarValoresPet: " + erro);
        }
    }

