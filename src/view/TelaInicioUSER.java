    Vector<Integer> id_situacao = new Vector<Integer>();

    public void restaurarDadosComboBoxSituacao() {

        try {

            Petdao objpetdao = new Petdao();
            ResultSet rs = objpetdao.listarSituacao();

            while (rs.next()) {
                id_situacao.addElement(rs.getInt(1));
                cbxSituacao.addItem(rs.getString(2));
                cbxEditSituacao.addItem(rs.getString(2));
            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "TelaInicioADM - Carregar Situacao Pet: " + erro);
        }
    }

    private void AlterarPet() {
        int id_pet, cod_sit;
        String pet_nome, pet_especie, pet_raca, pet_cor, pet_detalhes;

        id_pet = Integer.parseInt(txtEditIdPet.getText());
        pet_nome = textEditPetNome.getText();
        pet_especie = textEditPetEspecie.getText();
        pet_raca = textEditPetRaca.getText();
        pet_cor = textEditPetCor.getText();
        pet_detalhes = textEditDetal.getText();
        cod_sit = id_situacao.get(cbxEditSituacao.getSelectedIndex() - 1);

        Animal objpet = new Animal();

        objpet.setPet_id(id_pet);
        objpet.setPet_nome(pet_nome);
        objpet.setPet_especie(pet_especie);
        objpet.setPet_raca(pet_raca);
        objpet.setPet_cor(pet_cor);
        objpet.setPet_detalhes(pet_detalhes);
        objpet.setPet_sit(cod_sit);

        Petdao objpetdao = new Petdao();

        objpetdao.alterarPet(objpet);

    }

